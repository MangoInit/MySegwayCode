/************************串口1/3中断回掉函数**********************************/
void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
	if(huart->Instance == huart3.Instance)
	{
		JY901_UART3();
	}
	if(huart->Instance == huart1.Instance)
	{
		PID_UART1();
	}
}

//串口1中断回掉 PID参数调试函数
#define Danger 0x00
#define PD_P 0x11
#define PD_D 0x22
#define PI_P 0x33
#define PI_I 0x44

uint8_t Rx_Data1;					//接受一个数据	
uint8_t RxBuffer1[5];			//串口3接受JY901陀螺仪数据
uint8_t RxCnt1;						//数据包个数

//串口1发送十六进制PID参数
void PID_UART1()
{
	RxBuffer1[RxCnt1] = Rx_Data1;
	HAL_UART_Receive_IT(&huart1, (uint8_t *)&Rx_Data1, 1);				//重新打开串口1接收中断
	RxCnt1++;
	if(RxBuffer1[0]!=0x88)											//数据头校核
	{
		RxCnt1 = 0;
		return;
	}
	if(RxCnt1 < 5)
	{
		return;
	}
	if(RxCnt1 == 5)															//数据尾校核
	{
		if(RxBuffer1[4]!=0x99)
		{
			RxCnt1 = 0;
			return;
		}
		else
		{
			switch(RxBuffer1[1])
			{
				case PD_P: Balance_PD_P = RxBuffer1[2]<<8 | RxBuffer1[3]; break;
				case PD_D: Balance_PD_D = RxBuffer1[2]<<8 | RxBuffer1[3]; break;
				case PI_P: Velocity_PI_P = RxBuffer1[2]<<8 | RxBuffer1[3];break;
				case PI_I: Velocity_PI_I = RxBuffer1[2]<<8 | RxBuffer1[3];break;
				case Danger: Danger_Flag = 1; break;
				
				case 0x50: Stop_Flag = 0; Qian_Flag = 0; Hou_Flag  = 0; Left_Flag = 0; Right_Flag= 0; break;//启动电机
				case 0x51: Stop_Flag = 1; Qian_Flag = 0; Hou_Flag  = 0; Left_Flag = 0; Right_Flag= 0; break;//关闭电机
			
				case 0x52: Qian_Flag = 0; Hou_Flag  = 0; Left_Flag = 0; Right_Flag= 0; break;		//刹车
				case 0x53: Qian_Flag = 1; Hou_Flag  = 0; break;		//前进
				case 0x54: Qian_Flag = 0; Hou_Flag  = 1; break;		//后退
				case 0x55: Left_Flag = 1; Right_Flag= 0; break;		//左转
				case 0x56: Left_Flag = 0; Right_Flag= 1; break;		//右转
				
				case 0x57: Stop_Flag = 0; ZHONGZHI = Get_JY901_Init_UART(); break;		//初始化启动，记录启动时陀螺仪位置为机械中值
				case 0x58: ZHONGZHI = Get_JY901_Init_UART(); break;					//只记录启动陀螺仪位置
				default: RxCnt1 = 0; break;
			}
			RxCnt1 = 0;
		}
	}
}

