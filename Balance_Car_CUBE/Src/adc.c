//获ADC平均值
//times:平均次数
uint16_t Get_Times_ADC(uint8_t times)
{
	uint16_t ADC_Value = 0;
	uint32_t ADC_Value_All = 0;
	uint8_t  t;
	for(t = 0; t<times; t++)
	{
		HAL_ADC_Start(&hadc2);
		HAL_ADC_PollForConversion(&hadc2, 5);
		if(HAL_IS_BIT_SET(HAL_ADC_GetState(&hadc2), HAL_ADC_STATE_REG_EOC))
		{
			ADC_Value = HAL_ADC_GetValue(&hadc2);
		}
		ADC_Value_All += ADC_Value;
	}
//	printf("adc = %d\r\n",ADC_Value_All/times);
	return ADC_Value_All/times;
}

//记录启动时转向轴位置为初始位置
void Steer_Init()
{
	Init_ADC = Get_Times_ADC(20);
	printf("Init_ADC = %d\r\n", Init_ADC);
}

//获取转向车把转换值
uint16_t Get_Turn_ADC()
{
	int32_t Turn_ADC;
	uint16_t Get_ADC;
	Get_ADC = Get_Times_ADC(10);
	Turn_ADC = Get_ADC - Init_ADC;
//	printf(" turn adc = %d\r\n", Turn_ADC/10);
	return Turn_ADC/10;
}

//获取芯片内部温度
uint8_t Get_Temperature_10bei()
{
	double temp,temperature;
	uint16_t ADC_Value;
	
	HAL_ADC_Start(&hadc1);
	HAL_ADC_PollForConversion(&hadc1, 10);
	if(HAL_IS_BIT_SET(HAL_ADC_GetState(&hadc1), HAL_ADC_STATE_REG_EOC))
	{
		ADC_Value = HAL_ADC_GetValue(&hadc1);
		temp = (float)ADC_Value*(3.3/4096);
		temperature = (temp - 1.43)/0.0043 + 25;
		printf("MCU Temperature: %f         ",temperature);
		printf("%d    %f\r\n",ADC_Value,temp);
	}
	return temperature*10;
}

